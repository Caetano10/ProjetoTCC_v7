    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed
        ProdutoEstoque pe = new ProdutoEstoque();
        ProdutoEstoqueDAO pedao = new ProdutoEstoqueDAO();
        Fornecedor f = new Fornecedor();
        pe.setNomeProd(txtnomeprod.getText());
        pe.setCodBarras(txtcodbarras.getText());
        
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate d = LocalDate.parse(txtdtentrada.getText(), formato);        
        pe.setDataEntrada(d.toString());
        
        pe.setQtde(Integer.parseInt(txtqtd.getText()));
        f.setNomeFantasia(cbforn.getSelectedItem().toString());
        pe.setFornecedor(f);
        pedao.adicionar(pe,f);
        JOptionPane.showMessageDialog(null, "Produto Cadastro com Sucesso");
    }//GEN-LAST:event_btncadastrarActionPerformed

    private void cbfornAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_cbfornAncestorAdded
        FornecedorDAO fdao = new FornecedorDAO();
        List<Fornecedor> fornecedores = fdao.listar2();
        DefaultComboBoxModel model=(DefaultComboBoxModel)cbforn.getModel();
        model.removeAllElements();
        for (Fornecedor f:fornecedores) {
            model.addElement(f.getNomeFantasia());
        }
    }//GEN-LAST:event_cbfornAncestorAdded

