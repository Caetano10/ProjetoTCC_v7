    private void btnalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnalterarActionPerformed
         int alt = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar este cadastro?");
        Funcionario f = new Funcionario();
        FuncionarioDAO fdao = new FuncionarioDAO();
        Endereco e = new Endereco();
        if (alt == 0) {
            f.setNome(txtNome.getText());
            f.setCpf(txtCpf.getText());
            f.setTelefone(txtTel.getText());
            f.setDataNascimento(txtdtnasc.getText());
            e.setUnidadeFederal(txtestado.getText());
            e.setCidade(txtCidade.getText());
            e.setBairro(txtBairro.getText());
            e.setRua(txtRua.getText());
            e.setLogradouroNum(Integer.parseInt(txtLogradouro.getText()));
            e.setCep(txtcep.getText());
            e.setComplemento(txtcomplemento.getText());

            fdao.alterar(f, e);
            JOptionPane.showMessageDialog(null, "Fornecedor alterado com sucesso");
            txtNome.setText("");
            txtCpf.setText("");
            txtTel.setText("");
            txtestado.setText("");
            txtCidade.setText("");
            txtBairro.setText("");
            txtRua.setText("");
            txtLogradouro.setText("");
            txtcep.setText("");
            txtcomplemento.setText("");
            txtDtCad.setText("");
            txtdtnasc.setText("");
        }
    }//GEN-LAST:event_btnalterarActionPerformed

