    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        Funcionario f = new Funcionario();
        FuncionarioDAO fdao = new FuncionarioDAO();
        int r = JOptionPane.showConfirmDialog(null, "Voce realmente deseja apagar este registro?");
        if (r == 0){
            f.setIdFuncionario(Integer.parseInt(lblcod.getText()));
            fdao.remover(f);
            txtNome.setText("");
            txtCpf.setText("");
            txtTel.setText("");
            txtestado.setText("");
            txtCidade.setText("");
            txtBairro.setText("");
            txtRua.setText("");
            txtLogradouro.setText("");
            txtcep.setText("");
            txtcomplemento.setText("");
            txtDtCad.setText("");
            txtdtnasc.setText("");
        }
        
    }//GEN-LAST:event_btnexcluirActionPerformed

