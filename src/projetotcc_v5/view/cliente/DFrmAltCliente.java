    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        Cliente c = new Cliente();
        int r = JOptionPane.showConfirmDialog(null, "Você realmente deseja remover este cadastro?");
        if(r == 0) {
            c.setIdCliente(Integer.parseInt(lblid.getText()));
            
            ClienteDAO cdao = new ClienteDAO();
            cdao.remover(c);
                txtNome.setText("");
                txtCpf.setText("");
                txtCel.setText("");
                txtTel.setText("");
                txtDtCad.setText("");
        }

        
    }//GEN-LAST:event_btnexcluirActionPerformed

    private void btnalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnalterarActionPerformed
        Cliente c = new Cliente();
        int r = JOptionPane.showConfirmDialog(null, "Você realmente deseja alterar este cadastro?");
        if(r == 0) {
            c.setIdCliente(Integer.parseInt(lblid.getText()));
            c.setNome(txtNome.getText());
            c.setCpf(txtCpf.getText());
            c.setTelefone(txtTel.getText());
            c.setCelular(txtCel.getText());
            c.setDataNascimento(txtDataNascimento.getText());

            ClienteDAO cdao = new ClienteDAO();
            cdao.alterar(c);
            JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso.");
            txtNome.setText("");
            txtCpf.setText("");
            txtCel.setText("");
            txtTel.setText("");
            txtDtCad.setText("");
        }
    }//GEN-LAST:event_btnalterarActionPerformed

