    private void tblcarrosAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_tblcarrosAncestorAdded
    //listar a pesquisa
        CarroDAO d = new CarroDAO();
        List<Carro> carros = d.listar("idClienteFK", lblid.getText());
        DefaultTableModel model = (DefaultTableModel) tblcarros.getModel();
        model.setNumRows(0);
        for (Carro c : carros) {
            model.addRow(new Object[]{
                c.getIdCarro(),
                c.getPlacaVeiculo(),
                c.getNomeVeiculo(),
                c.getCorVeiculo(),
                c.getObservacoes(),
                c.getDetalhesLavagem(),
                c.getIdClienteFK()
            });

        }
    }//GEN-LAST:event_tblcarrosAncestorAdded

    private void tblcarrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcarrosMouseClicked
        if (evt.getClickCount() == 1) {
            DFrmAltCarro f = new DFrmAltCarro(null, rootPaneCheckingEnabled);
            f.lblId.setText(tblcarros.getValueAt(tblcarros.getSelectedRow(),0).toString());
            f.setVisible(true);
           
        }
    }//GEN-LAST:event_tblcarrosMouseClicked

